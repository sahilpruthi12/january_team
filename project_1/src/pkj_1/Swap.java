package pkj_1;

public class Swap 
{
	public static void main(String[] args) 
	{
	int a=12;
	int b=15;
	System.out.println("value of a is"+a);
	System.out.println("value of b is"+b);
	int c=a;
	a=b;
	b=c;
	System.out.println("value of a is now"+a);
	System.out.println("value of b is now"+b);
	}
	}
